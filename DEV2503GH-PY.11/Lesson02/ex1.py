# Toán tử số học trong python
# *; /; +; -; %; // ; **

# Nhập giá trị
a = input("Nhập số a=")
b = input("Nhập số b=")
# Chuyển sang kiểu số
a = int(a)
b = int(b)
# Phép cộng +
result = a+b
print (result)

# Phép -
result = a - b
print(a, "-" , b ,"=",result)

# Phép *
result = a * b
print(a, "*" , b ,"=",result)

# Phép /
result = a / b
print(a, "/" , b ,"=",result)

# Phép %
result = a % b
print(a, "%" , b ,"=",result)

# Phép //
result = a // b
print(a, ":" , b ,"=",result)

# Phép ** (Mũ)
result = a ** b
print(a, "^" , b ,"=",result)
