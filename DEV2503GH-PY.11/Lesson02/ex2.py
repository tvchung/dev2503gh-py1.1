# Các toán tử so sánh
# >; >=; <; <=; ==; !=

a = int(input("Nhập a="))
b = int(input("Nhập b="))

res = a == b
print(a,"=",b,"=>",res)

res = a > b
print(a,">",b,"=>",res)


res = a >= b
print(a,">=",b,"=>",res)

res = a < b
print(a,"<",b,"=>",res)

res = a <= b
print(a,"<=",b,"=>",res)

res = a != b
print(a,"!=",b,"=>",res)

